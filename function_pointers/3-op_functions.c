#include "3-calc.h"

/**
 * op_add - return sum of a and b
 * @a: first number to math
 * @b: second number to math
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return difference of a and b
 * @a: first number to math
 * @b: second number to math
 * Return: differance of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return product of a and b
 * @a: first number to math
 * @b: second number to math
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return devsion of a and b
 * @a: first number to math
 * @b: second number to math
 * Return: devison of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return remainder of a and b
 * @a: first number to math
 * @b: second number to math
 * Return: remainder of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
